const {Router} =require('express')
const {registerUser,cheackEmail,checkPassword,userDetails,logout,updateProfilePic,updateUser,searchUser}=require('../controller/userController')
const {createGroup,addMember,leftGroup,groupDetails,editName,editProfilePic,remove}=require('../controller/groupChatController')
const router=Router()
router.post('/register',registerUser)
router.post('/email',cheackEmail)
router.post('/password',checkPassword)
router.get('/user',userDetails)
router.get('/logout',logout)
router.post('/update-user-profile',updateProfilePic)
router.patch('/update-user',updateUser)
router.post('/search-user',searchUser)
router.post('/group',createGroup)
router.get('/group/:id',groupDetails)
router.patch('/group/:id',editName)
router.put('/group/:id',editProfilePic)
router.post('/group/member/:id',addMember)
router.delete('/group/member/:id',leftGroup)
router.patch('/group/member/:id',remove)
module.exports=router